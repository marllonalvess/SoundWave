    private void TxFEstoqueKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxFEstoqueKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_TxFEstoqueKeyTyped

    private void TxFValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxFValorKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        if (Character.isDigit(c)) {
            return;
        }

        if (c == ',' && !TxFValor.getText().contains(",")) {
            return;
        }

        evt.consume();
    }//GEN-LAST:event_TxFValorKeyTyped

