    private void BtnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadActionPerformed
        // TODO add your handling code here:
        // validacao campo modelo

        try {
            // validacoes
            if (TxfModelo.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o nome!");
                TxfModelo.requestFocus();
                return;
            }

            if (TxFValor.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o valor!");
                TxFValor.requestFocus();
                return;
            }

            if (TxFEstoque.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o estoque!");
                TxFEstoque.requestFocus();
                return;
            }

            if (ComboCat.getSelectedItem().equals("Selecione")) {
                JOptionPane.showMessageDialog(this, "Selecione a categoria!");
                return;
            }

            if (ComboTipo.getSelectedItem().equals("Selecione")) {
                JOptionPane.showMessageDialog(this, "Selecione o tipo!");
                return;
            }

            if (ComboMarca.getSelectedItem().equals("Selecione")) {
                JOptionPane.showMessageDialog(this, "Selecione a marca!");
                return;
            }
            
            Produto p = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            boolean status;
            int resposta;

            p.setModelo(TxfModelo.getText());
            p.setCategoria(ComboCat.getSelectedItem().toString());
            p.setTipo(ComboTipo.getSelectedItem().toString());
            p.setMarca(ComboMarca.getSelectedItem().toString());
            p.setValor(Double.parseDouble(TxFValor.getText().trim().replace(",", ".")));
            p.setEstoque(Integer.parseInt(TxFEstoque.getText()));

            dao = new ProdutoDAO();
            status = dao.conectar();

            if (status == false) {
                JOptionPane.showMessageDialog(null, "Erro ao conectar.");
            } else {

                if (modoEdicao) {
                    p.setId(idProdutoEdicao);
                    resposta = dao.atualizar(p);
                    JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");
                } else {
                    resposta = dao.salvar(p);
                    JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
                }
            }
            TelaProdutos t = new TelaProdutos(usuarioLogado);
            t.setVisible(true);
            this.dispose();
            dao.desconectar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro inesperado: " + e.getMessage());
        }

    }//GEN-LAST:event_BtnCadActionPerformed

