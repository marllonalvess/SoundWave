    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linhaView = JTableClientes.getSelectedRow();
        int id = Integer.parseInt(JTableClientes.getValueAt(linhaView, 0).toString());

        if (linhaView == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente!");
            return;
        }

        int linhaModel = JTableClientes.convertRowIndexToModel(linhaView);

        int opcao = JOptionPane.showConfirmDialog(
                this,
                "Deseja realmente excluir?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {
            ClienteDAO dao = new ClienteDAO();
            dao.excluir(id);
        } else {
            return;
        }
        ClienteDAO daoC = new ClienteDAO();
        daoC.listar(JTableClientes);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int linhaView = JTableClientes.getSelectedRow();
        if (linhaView == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um produto!");
            return;
        }

        int linhaModel = JTableClientes.convertRowIndexToModel(linhaView);
        DefaultTableModel model = (DefaultTableModel) JTableClientes.getModel();
        int id = Integer.parseInt(model.getValueAt(linhaModel, 0).toString());

        String nome = model.getValueAt(linhaModel, 1).toString();
        String cpf = model.getValueAt(linhaModel, 2).toString();
        String telefone = model.getValueAt(linhaModel, 3).toString();

        TelaCadCliente c = new TelaCadCliente(usuarioLogado);
        c.ativarEdicao(id, nome, cpf, telefone);
        c.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

