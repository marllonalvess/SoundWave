    private void TxfBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxfBuscaKeyReleased
        // TODO add your handling code here:
        String texto = TxfBusca.getText().trim();

        if (texto.isEmpty()) {
            sorterClientes.setRowFilter(null);
        } else {
            sorterClientes.setRowFilter(
                    RowFilter.regexFilter("(?i)" + texto, 0)
            );
        }
    }//GEN-LAST:event_TxfBuscaKeyReleased

